    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        String nombre = this.txtNombre.getText();

        try {
            double precio = Double.parseDouble(this.txtPrecio.getText());
            this.plato.setNombre(nombre);
            this.plato.setPrecioDeVenta(precio);

            this.restaurante.actualizarPlato(this.plato.getIdentificadorUnico(), this.plato);
            JOptionPane.showMessageDialog(this, "Producto " + nombre + " ha sido actualizado exitosamente");
            this.ventanaMenu.llenarTabla();
            this.dispose();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese un n√∫mero en el campo precio");
            this.txtPrecio.setText("");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

